package com.vts.pfms.milestone.dto;

public class FileProjectDocDto {


	private String ProjectDocId;
	private String Projectid;
	private String[] FileUploadMasterId;
	private String CreatedBy;
	private String CreatedDate;
	
	public String getProjectDocId() {
		return ProjectDocId;
	}
	public void setProjectDocId(String projectDocId) {
		ProjectDocId = projectDocId;
	}
	public String getProjectid() {
		return Projectid;
	}
	public void setProjectid(String projectid) {
		Projectid = projectid;
	}
	public String[] getFileUploadMasterId() {
		return FileUploadMasterId;
	}
	public void setFileUploadMasterId(String[] fileUploadMasterId) {
		FileUploadMasterId = fileUploadMasterId;
	}
	public String getCreatedBy() {
		return CreatedBy;
	}
	public void setCreatedBy(String createdBy) {
		CreatedBy = createdBy;
	}
	public String getCreatedDate() {
		return CreatedDate;
	}
	public void setCreatedDate(String createdDate) {
		CreatedDate = createdDate;
	}
	
	
	
	
	
}
